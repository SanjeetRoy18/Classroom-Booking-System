-- 5. TRIGGERS
-- ────────────────────────────────────────────────────────────
CREATE OR REPLACE TRIGGER prevent_overlap
BEFORE INSERT ON Bookings
FOR EACH ROW
DECLARE
    v_count NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_count
    FROM Bookings
    WHERE room_id      = :NEW.room_id
      AND booking_date = :NEW.booking_date
      AND status_id   != 3
      AND start_time   < :NEW.end_time
      AND end_time     > :NEW.start_time;

    IF v_count > 0 THEN
        RAISE_APPLICATION_ERROR(-20001, 'Room already booked for that time slot.');
    END IF;
END;
/

CREATE OR REPLACE TRIGGER faculty_schedule_conflict
BEFORE INSERT ON Bookings
FOR EACH ROW
DECLARE
    v_faculty_conflict NUMBER;
BEGIN
    SELECT COUNT(*) INTO v_faculty_conflict
    FROM Bookings
    WHERE faculty_id   = :NEW.faculty_id
      AND booking_date = :NEW.booking_date
      AND status_id   != 3
      AND start_time   < :NEW.end_time
      AND end_time     > :NEW.start_time;

    IF v_faculty_conflict > 0 THEN
        RAISE_APPLICATION_ERROR(-20004, 'Faculty schedule conflict: You are already teaching in another room during this exact time slot.');
    END IF;
END;
/

CREATE OR REPLACE TRIGGER notify_on_booking
AFTER INSERT ON Bookings
FOR EACH ROW
BEGIN
    -- Notify the Faculty who booked it
    INSERT INTO Notifications (user_id, message, is_read)
    VALUES (:NEW.faculty_id, 'Booking Confirmed for course ' || NVL(:NEW.course_id, 'General') || ' in room ' || :NEW.room_id, 0);
    
    -- Broadcast to all Enrolled Students
    IF :NEW.course_id IS NOT NULL THEN
        FOR student_rec IN (SELECT student_id FROM Enrollments WHERE course_id = :NEW.course_id) LOOP
            INSERT INTO Notifications (user_id, message, is_read)
            VALUES (student_rec.student_id, 'New class scheduled: ' || :NEW.course_id || ' in room ' || :NEW.room_id || ' (' || TO_CHAR(:NEW.booking_date, 'Mon DD') || ')', 0);
        END LOOP;
    END IF;
END;
/

CREATE OR REPLACE TRIGGER notify_cancel
AFTER UPDATE ON Bookings
FOR EACH ROW
WHEN (NEW.status_id = 3 AND OLD.status_id != 3)
BEGIN
    -- Notify the Faculty
    INSERT INTO Notifications (user_id, message, is_read)
    VALUES (:NEW.faculty_id, 'Your booking for room ' || :NEW.room_id || ' has been cancelled.', 0);
    
    -- Broadcast Cancellation to Enrolled Students
    IF :NEW.course_id IS NOT NULL THEN
        FOR student_rec IN (SELECT student_id FROM Enrollments WHERE course_id = :NEW.course_id) LOOP
            INSERT INTO Notifications (user_id, message, is_read)
            VALUES (student_rec.student_id, 'ALERT: Class Cancelled constraints for ' || :NEW.course_id || ' on ' || TO_CHAR(:NEW.booking_date, 'Mon DD'), 0);
        END LOOP;
    END IF;
END;
/

CREATE OR REPLACE TRIGGER check_periodic_dates
BEFORE INSERT ON PeriodicDetails
FOR EACH ROW
BEGIN
    IF :NEW.end_date < TRUNC(SYSDATE) THEN
        RAISE_APPLICATION_ERROR(-20002, 'End date for recurring booking must be today or in the future.');
    END IF;
END;
/

CREATE OR REPLACE TRIGGER audit_bookings
AFTER UPDATE OR DELETE ON Bookings
FOR EACH ROW
DECLARE
    v_action VARCHAR2(20);
BEGIN
    IF DELETING THEN
        v_action := 'DELETE';
    ELSIF UPDATING THEN
        v_action := 'UPDATE';
    END IF;
    
    INSERT INTO Booking_Audit (booking_id, action)
    VALUES (NVL(:OLD.booking_id, :NEW.booking_id), v_action);
END;
/

