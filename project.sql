-- 2. LOOKUP / REFERENCE TABLES
-- ────────────────────────────────────────────────────────────
CREATE TABLE Roles (
    role_id   NUMBER PRIMARY KEY,
    role_name VARCHAR2(50) NOT NULL
);

CREATE TABLE BookingStatuses (
    status_id   NUMBER PRIMARY KEY,
    status_name VARCHAR2(50) NOT NULL
);

CREATE TABLE BookingTypes (
    type_id   NUMBER PRIMARY KEY,
    type_name VARCHAR2(50) NOT NULL
);

CREATE TABLE DaysOfWeek (
    day_id   NUMBER PRIMARY KEY,
    day_name VARCHAR2(10) NOT NULL
);

CREATE TABLE NotifTypes (
    notif_type_id NUMBER PRIMARY KEY,
    type_name     VARCHAR2(50) NOT NULL
);

CREATE TABLE Semesters (
    semester_id NUMBER PRIMARY KEY,
    name        VARCHAR2(50),
    start_date  DATE,
    end_date    DATE
);

-- ────────────────────────────────────────────────────────────
-- 3. CORE DOMAIN TABLES
-- ────────────────────────────────────────────────────────────
CREATE TABLE Departments (
    dept_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name    VARCHAR2(100)
);

CREATE TABLE Users (
    user_id    NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name       VARCHAR2(100) NOT NULL,
    email      VARCHAR2(100) UNIQUE NOT NULL,
    role_id    NUMBER REFERENCES Roles(role_id),
    phone      VARCHAR2(15),
    password   VARCHAR2(100) NOT NULL,
    role       VARCHAR2(20),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE Classrooms (
    room_id       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    building      VARCHAR2(50),
    room_number   VARCHAR2(10) NOT NULL,
    capacity      NUMBER CHECK (capacity > 0),
    has_projector NUMBER(1) DEFAULT 0,
    has_ac        NUMBER(1) DEFAULT 0
);

CREATE TABLE Courses (
    course_id VARCHAR2(20) PRIMARY KEY,
    title     VARCHAR2(100) NOT NULL,
    dept_id   NUMBER REFERENCES Departments(dept_id),
    credits   NUMBER CHECK (credits > 0)
);

CREATE TABLE Bookings (
    booking_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    faculty_id      NUMBER NOT NULL REFERENCES Users(user_id),
    room_id         NUMBER NOT NULL REFERENCES Classrooms(room_id),
    course_id       VARCHAR2(20) REFERENCES Courses(course_id),
    booking_type_id NUMBER REFERENCES BookingTypes(type_id),
    booking_date    DATE NOT NULL,
    start_time      DATE NOT NULL,
    end_time        DATE NOT NULL,
    semester_id     NUMBER REFERENCES Semesters(semester_id),
    status_id       NUMBER DEFAULT 1 REFERENCES BookingStatuses(status_id),
    created_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_times CHECK (end_time > start_time)
);

CREATE TABLE PeriodicDetails (
    periodic_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    booking_id  NUMBER NOT NULL REFERENCES Bookings(booking_id) ON DELETE CASCADE,
    day_id      NUMBER NOT NULL REFERENCES DaysOfWeek(day_id),
    end_date    DATE NOT NULL
);

CREATE TABLE Enrollments (
    enrollment_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    student_id    NUMBER NOT NULL REFERENCES Users(user_id),
    course_id     VARCHAR2(20) NOT NULL REFERENCES Courses(course_id),
    semester_id   NUMBER REFERENCES Semesters(semester_id),
    CONSTRAINT uq_enrollment UNIQUE (student_id, course_id, semester_id)
);

CREATE TABLE Cancellations (
    cancel_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    booking_id     NUMBER NOT NULL REFERENCES Bookings(booking_id),
    cancelled_by   NUMBER,
    cancelled_date DATE DEFAULT SYSDATE,
    reason         VARCHAR2(200)
);

CREATE TABLE Notifications (
    notif_id      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id       NUMBER NOT NULL,
    message       VARCHAR2(200) NOT NULL,
    notif_type_id NUMBER REFERENCES NotifTypes(notif_type_id),
    is_read       NUMBER(1) DEFAULT 0,
    created_at    TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE Booking_Audit (
    audit_id   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    booking_id NUMBER,
    action     VARCHAR2(20),
    changed_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- ────────────────────────────────────────────────────────────
-- 4. VIEWS
-- ────────────────────────────────────────────────────────────
CREATE OR REPLACE VIEW BookingDetails AS
SELECT
    b.booking_id,
    u.name,
    c.room_number,
    b.booking_date,
    b.start_time,
    b.end_time,
    s.status_name
FROM Bookings b
JOIN Users u ON b.faculty_id = u.user_id
JOIN Classrooms c ON b.room_id = c.room_id
JOIN BookingStatuses s ON b.status_id = s.status_id
WHERE b.status_id != 3;

CREATE OR REPLACE VIEW RoomUsage AS
SELECT
    room_id,
    COUNT(*) AS total_bookings
FROM Bookings
GROUP BY room_id;

CREATE OR REPLACE VIEW Dept_Capacity_Report AS
SELECT 
    NVL(d.name, 'All Departments') AS department,
    NVL(c.building, 'All Buildings') AS building,
    SUM(c.capacity) AS total_seat_capacity,
    COUNT(c.room_id) AS total_rooms
FROM Classrooms c
LEFT JOIN Bookings b ON c.room_id = b.room_id
LEFT JOIN Courses cr ON b.course_id = cr.course_id
LEFT JOIN Departments d ON cr.dept_id = d.dept_id
GROUP BY ROLLUP(d.name, c.building);

