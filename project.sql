-- Scalar Function: Calculate Total Faculty Hours
CREATE OR REPLACE FUNCTION GET_FACULTY_WORKLOAD(p_faculty_id NUMBER)
RETURN NUMBER
IS
    v_total_hours NUMBER := 0;
BEGIN
    SELECT NVL(SUM((end_time - start_time) * 24), 0)
    INTO v_total_hours
    FROM Bookings
    WHERE faculty_id = p_faculty_id AND status_id != 3;
    
    RETURN v_total_hours;
END;
/

-- Procedure demonstrating Explicit Parameterized Cursors
CREATE OR REPLACE PROCEDURE Generate_Course_Roster(p_course_id IN VARCHAR2)
IS
    CURSOR c_students(course_id_in VARCHAR2) IS
        SELECT u.name, u.email
        FROM Enrollments e
        JOIN Users u ON e.student_id = u.user_id
        WHERE e.course_id = course_id_in;
        
    v_student_rec c_students%ROWTYPE;
    v_count NUMBER := 0;
BEGIN
    DBMS_OUTPUT.PUT_LINE('--- Course Roster for ' || p_course_id || ' ---');
    
    OPEN c_students(p_course_id);
    LOOP
        FETCH c_students INTO v_student_rec;
        EXIT WHEN c_students%NOTFOUND;
        
        DBMS_OUTPUT.PUT_LINE('Student Name: ' || v_student_rec.name || ' | Email: ' || v_student_rec.email);
        v_count := v_count + 1;
    END LOOP;
    CLOSE c_students;
    
    DBMS_OUTPUT.PUT_LINE('Total Enrolled: ' || v_count);
END;
/

-- PL/SQL Package Encapsulation (Specification)
CREATE OR REPLACE PACKAGE Booking_Manager AS
    PROCEDURE Smart_Book(p_user NUMBER, p_room NUMBER, p_date DATE, p_start DATE, p_end DATE);
    PROCEDURE Cancel_Booking(p_booking_id NUMBER, p_user_id NUMBER, p_reason VARCHAR2);
END Booking_Manager;
/

-- PL/SQL Package Encapsulation (Body)
CREATE OR REPLACE PACKAGE BODY Booking_Manager AS
    -- Standard booking procedure implemented inside package
    PROCEDURE Smart_Book(p_user NUMBER, p_room NUMBER, p_date DATE, p_start DATE, p_end DATE)
    IS
        v_conflict NUMBER;
    BEGIN
        SELECT COUNT(*) INTO v_conflict
        FROM Bookings
        WHERE room_id      = p_room
          AND booking_date = p_date
          AND status_id   != 3
          AND start_time   < p_end
          AND end_time     > p_start;

        IF v_conflict > 0 THEN
            RAISE_APPLICATION_ERROR(-20001, 'Conflict detected: room already booked.');
        END IF;

        INSERT INTO Bookings (faculty_id, room_id, booking_date, start_time, end_time, status_id)
        VALUES (p_user, p_room, p_date, p_start, p_end, 1);
        COMMIT;
    END Smart_Book;

    -- Standard cancellation procedure natively handling Audit Triggers
    PROCEDURE Cancel_Booking(p_booking_id NUMBER, p_user_id NUMBER, p_reason VARCHAR2)
    IS
        v_owner NUMBER;
    BEGIN
        SELECT faculty_id INTO v_owner FROM Bookings WHERE booking_id = p_booking_id;
        
        IF v_owner != p_user_id THEN
            RAISE_APPLICATION_ERROR(-20003, 'Unauthorized cancellation attempt.');
        END IF;
        
        UPDATE Bookings SET status_id = 3 WHERE booking_id = p_booking_id;
        INSERT INTO Cancellations (booking_id, cancelled_by, reason) 
        VALUES (p_booking_id, p_user_id, NVL(p_reason, 'Cancelled via PL/SQL Manager'));
        COMMIT;
    END Cancel_Booking;
END Booking_Manager;
/

