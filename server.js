// POST /book — Book a room
// ─────────────────────────────────────────
app.post("/book", async (req, res) => {
  const { faculty_id, room_id, course_id, date, start, end, type_id, end_date } = req.body;
  let conn;
  try {
    conn = await oracledb.getConnection(dbConfig);
    const result = await conn.execute(
      `INSERT INTO Bookings (faculty_id, room_id, course_id, booking_type_id, booking_date, start_time, end_time, status_id)
       VALUES (:f_id, :r_id, :c_id, :t_id, TO_DATE(:b_date, 'YYYY-MM-DD'), 
               TO_DATE(:b_date || ' ' || :s, 'YYYY-MM-DD HH24:MI'), 
               TO_DATE(:b_date || ' ' || :e, 'YYYY-MM-DD HH24:MI'), 1)
       RETURNING booking_id INTO :id`,
      {
        f_id: faculty_id, r_id: room_id, c_id: course_id || null, t_id: type_id || 1, 
        b_date: date, s: start, e: end, id: { type: oracledb.NUMBER, dir: oracledb.BIND_OUT }
      }
    );

    const newBookingId = result.outBinds.id[0];

    // Recurring Setup
    if (type_id == 2 && end_date) {
      const dayId = new Date(date).getDay() + 1; 
      await conn.execute(
        `INSERT INTO PeriodicDetails (booking_id, day_id, end_date)
         VALUES (:b_id, :d_id, TO_DATE(:e_date, 'YYYY-MM-DD'))`,
        { b_id: newBookingId, d_id: dayId, e_date: end_date }
      );
    }
    await conn.commit();
    res.send("Booking successful!");
  } catch (err) { res.status(500).send(err.message);
  } finally { if (conn) await conn.close(); }
});

