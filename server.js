async function checkConn() {
  let c;
  try {
    c = await oracledb.getConnection(dbConfig);
    console.log("✅ Oracle Connection Successful!");
    
    // --- AUTO-PATCH MISSING TABLES ---
    const patches = [
      `CREATE TABLE BookingTypes (type_id NUMBER PRIMARY KEY, type_name VARCHAR2(50) NOT NULL)`,
      `CREATE TABLE DaysOfWeek (day_id NUMBER PRIMARY KEY, day_name VARCHAR2(10) NOT NULL)`,
      `CREATE TABLE PeriodicDetails (periodic_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, booking_id NUMBER NOT NULL REFERENCES Bookings(booking_id) ON DELETE CASCADE, day_id NUMBER NOT NULL REFERENCES DaysOfWeek(day_id), end_date DATE NOT NULL)`,
      `CREATE TABLE Cancellations (cancel_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, booking_id NUMBER NOT NULL REFERENCES Bookings(booking_id), cancelled_by NUMBER, cancelled_date DATE DEFAULT SYSDATE, reason VARCHAR2(200))`,
      `INSERT INTO BookingTypes (type_id, type_name) VALUES (1, 'One-time')`,
      `INSERT INTO BookingTypes (type_id, type_name) VALUES (2, 'Recurring')`,
      `INSERT INTO DaysOfWeek (day_id, day_name) VALUES (1, 'Sunday')`,
      `INSERT INTO DaysOfWeek (day_id, day_name) VALUES (2, 'Monday')`,
      `INSERT INTO DaysOfWeek (day_id, day_name) VALUES (3, 'Tuesday')`,
      `INSERT INTO DaysOfWeek (day_id, day_name) VALUES (4, 'Wednesday')`,
      `INSERT INTO DaysOfWeek (day_id, day_name) VALUES (5, 'Thursday')`,
      `INSERT INTO DaysOfWeek (day_id, day_name) VALUES (6, 'Friday')`,
      `INSERT INTO DaysOfWeek (day_id, day_name) VALUES (7, 'Saturday')`
    ];
    
    for(let sql of patches) {
      try { await c.execute(sql); await c.commit(); } catch(e) { } // Ignore if already exists
    }
    console.log("✅ Schema Verification Complete!");

  } catch (e) {
    console.error("❌ ORACLE CONNECTION FAILED:", e.message);
  } finally {
    if(c) await c.close();
  }
}
