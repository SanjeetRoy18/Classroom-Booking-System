// POST /cancel — Cancel a booking
// ─────────────────────────────────────────
app.post("/cancel", async (req, res) => {
  const { booking_id, user_id, reason } = req.body;
  if (!booking_id || !user_id) return res.status(400).send("Booking ID & User ID required.");
  
  let conn;
  try {
    conn = await oracledb.getConnection(dbConfig);
    const check = await conn.execute(`SELECT faculty_id FROM Bookings WHERE booking_id = :b_id`, { b_id: booking_id }, { outFormat: oracledb.OUT_FORMAT_OBJECT });
    if (check.rows.length === 0) return res.status(404).send("Booking not found.");
    if (check.rows[0].FACULTY_ID != user_id) return res.status(403).send("🚫 Unauthorized: You can only void your own bookings.");

    await conn.execute(`UPDATE Bookings SET status_id = 3 WHERE booking_id = :b_id`, { b_id: booking_id }, { autoCommit: false });
    await conn.execute(`INSERT INTO Cancellations (booking_id, cancelled_by, reason) VALUES (:b_id, :b_user, :b_rsn)`,
      { b_id: booking_id, b_user: user_id, b_rsn: reason || "Voided via Portal" }, { autoCommit: true });
    res.send("Booking voided.");
  } catch (err) { res.status(500).send(err.message);
  } finally { if (conn) await conn.close(); }
});

