// GET /timetable — True Recurring Projection
// ─────────────────────────────────────────
app.get("/timetable", async (req, res) => {
  let conn;
  try {
    conn = await oracledb.getConnection(dbConfig);
    const result = await conn.execute(
      `SELECT b.booking_id, u.name, b.course_id, c.room_number, t.type_name,
              b.booking_date,
              TO_CHAR(b.start_time, 'HH24:MI') AS start_time,
              TO_CHAR(b.end_time,   'HH24:MI') AS end_time,
              p.day_id, p.end_date as periodic_end
       FROM Bookings b
       JOIN Users u ON b.faculty_id = u.user_id
       JOIN Classrooms c ON b.room_id = c.room_id
       LEFT JOIN BookingTypes t ON b.booking_type_id = t.type_id
       LEFT JOIN PeriodicDetails p ON b.booking_id = p.booking_id
       WHERE b.status_id != 3`,
      [], { outFormat: oracledb.OUT_FORMAT_OBJECT }
    );
    
    // Project natively from JS Dates 
    let projectedTimetable = [];
    for(let row of result.rows) {
       let clone = {...row};
       projectedTimetable.push(clone);
       
       if(row.TYPE_NAME === 'Recurring' && row.PERIODIC_END) {
          if(row.BOOKING_DATE instanceof Date && row.PERIODIC_END instanceof Date) {
              let curr = new Date(row.BOOKING_DATE.getTime());
              let end = new Date(row.PERIODIC_END.getTime());
              
              curr.setDate(curr.getDate() + 7); // Start one week out
              
              let iterations = 0;
              while(curr <= end && iterations < 100) {
                  let newClone = {...row};
                  newClone.BOOKING_DATE = new Date(curr.getTime());
                  newClone.TYPE_NAME = 'Recurring (Projected)';
                  projectedTimetable.push(newClone);
                  curr.setDate(curr.getDate() + 7);
                  iterations++;
              }
          }
       }
    }
    
    // Safe memory sort
    projectedTimetable.sort((a,b) => {
       const da = a.BOOKING_DATE instanceof Date ? a.BOOKING_DATE.getTime() : 0;
       const db = b.BOOKING_DATE instanceof Date ? b.BOOKING_DATE.getTime() : 0;
       return da - db;
    });
    
    res.json(projectedTimetable);
  } catch (err) { 
    console.error("Timetable backend execution error: ", err);
    res.status(500).json({ error: err.message });
  } finally { if (conn) await conn.close(); }
});

